package Chapter1;

/**
 * Program is designed to find the perimeter and area of an object
 *
 * @author Emory Meursing
 */
public class C1_8 {

    /**
     * Main Method
     *
     * @param args arguments from command line prompt
     */
    public static void main(String[] args) {
        double perimeter = 2 * 5.5 * 3.14;
        double area = Math.pow(5.5, 2) * 3.14;
        System.out.println(perimeter);
        System.out.println(area);
    }

}
